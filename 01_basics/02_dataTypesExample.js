// Demonstrating JavaScript data types

// String
let name = "Rudra";

// Number
let age = 25;

// Boolean
let isStudent = true;

// Array
let hobbies = ["coding", "reading", "gaming"];

// Object
let person = {
    name: "Rudra",
    age: 25,
    isStudent: true
};

// Undefined
let undefinedVar;

// Null
let nullVar = null;

// Symbol
let uniqueId = Symbol("id");

// BigInt
let bigNumber = 123456789012345678901234567890n;

console.log("Data types examples:");
console.log("String:", name, typeof name);
console.log("Number:", age, typeof age);
console.log("Boolean:", isStudent, typeof isStudent);
console.log("Array:", hobbies, typeof hobbies);
console.log("Object:", person, typeof person);
console.log("Undefined:", undefinedVar, typeof undefinedVar);
console.log("Null:", nullVar, typeof nullVar);
console.log("Symbol:", uniqueId, typeof uniqueId);
// console.log("BigInt:", bigNumber, typeof bigNumber);